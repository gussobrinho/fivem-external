// ── Single weapon spawn via shellcode ─────────────────────
static bool SpawnWeapon(uintptr_t pedInventory,
    uintptr_t giveWeaponFunc,
    uint32_t  hash,
    uint32_t  ammo)
{
    std::vector<uint8_t> sc = {
        0x51,                                           // push rcx
        0x41, 0x50,                                     // push r8
        0x52,                                           // push rdx
        0x48, 0xB9,                                     // mov rcx, pedInventory
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,       // (offset 6)
        0x49, 0xB8,                                     // mov r8, ammo
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,       // (offset 16)
        0xBA,                                           // mov edx, hash
        0x00,0x00,0x00,0x00,                            // (offset 25)
        0xFF,0x15, 0x02,0x00,0x00,0x00,                // call [rip+2]
        0xEB, 0x08,                                     // jmp over ptr
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,       // (offset 37) fn ptr
        0x5A,                                           // pop rdx
        0x41, 0x58,                                     // pop r8
        0x59,                                           // pop rcx
        0xB8, 0x01,0x00,0x00,0x00,                     // mov eax, 1
        0xC3                                            // ret
    };

    // Patch in values
    memcpy(sc.data() + 6, &pedInventory, 8);
    uint64_t ammo64 = (uint64_t)ammo;
    memcpy(sc.data() + 16, &ammo64, 8);
    memcpy(sc.data() + 25, &hash, 4);
    memcpy(sc.data() + 37, &giveWeaponFunc, 8);

    uintptr_t cave = m.CreateCodeCave(sc.size());
    if (!cave) return false;

    if (!m.WriteBytes(cave, sc)) {
        m.FreeCave(cave);
        return false;
    }

    HANDLE hThread = CreateRemoteThread(
        m.ProcHandle, nullptr, 0,
        (LPTHREAD_START_ROUTINE)cave,
        nullptr, 0, nullptr);

    if (!hThread) {
        m.FreeCave(cave);
        return false;
    }

    WaitForSingleObject(hThread, 5000);

    DWORD exitCode = 0;
    GetExitCodeThread(hThread, &exitCode);
    CloseHandle(hThread);
    m.FreeCave(cave);

    return exitCode == 1;
}

// ── Public: GiveAllWeapons ─────────────────────────────────
void Cheat::GiveAllWeapons()
{
    if (!local.address) return;

    // Apply bypass once
    ApplyWeaponBypass();

    // Get CPedInventory pointerw
    uintptr_t pedInventory = m.Read<uintptr_t>(
        local.address + offset::m_pWeaponManager);
    if (!pedInventory) return;

    // GiveWeapon function address from your offsets
    static uintptr_t giveWeaponFunc = 0;
    if (!giveWeaponFunc)
        giveWeaponFunc = m.FindSignature({ 0x48, 0x89, 0x5c, 0x24, 0x00 , 0x48, 0x89, 0x6c, 0x24, 0x00 , 0x48, 0x89, 0x74, 0x24, 0x00 , 0x57, 0x48, 0x83, 0xec, 0x00 , 0x41, 0x8b, 0xf0, 0x8b, 0xfa, 0x48, 0x8b, 0xd9, 0xe8 });

    if (!giveWeaponFunc) return;

    // Spawn each weapon
    for (auto hash : Weapons::AllWeapons) {
        SpawnWeapon(pedInventory, giveWeaponFunc, hash, 9999);
        Sleep(15); // small delay between each
    }
}